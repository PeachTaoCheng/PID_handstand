/**
  * @brief This function handles TIM4 global interrupt.
  */
void TIM4_IRQHandler(void)
{
  /* USER CODE BEGIN TIM4_IRQn 0 */
  System_timer++;
  System_timer_angle++;
  System_timer_location++;
  /* USER CODE END TIM4_IRQn 0 */
  HAL_TIM_IRQHandler(&htim4);
  /* USER CODE BEGIN TIM4_IRQn 1 */

  //每5ms更新一次角度PID
  if(System_timer_angle >= 5)
  {
    System_timer_angle = 0;
    angle_pid.actual = RP_GetValue(1);
    if(pid_flag == 1)
    {
      pid_calc(&angle_pid);
      Motor_SetPWM((int16_t)angle_pid.output);
    }
    else
    {
      angle_pid.output = 0;
      Motor_SetPWM((int16_t)angle_pid.output);
    }
  }

  //每30ms更新一次位置PID
  if(System_timer_location >= 30)
  {
    System_timer_location = 0;
    int16_t count = Encoder_GetValue();
    location_pid.actual += count;
    pid_calc(&location_pid);
    angle_pid.target = angle_target + location_pid.output;
  }
  /* USER CODE END TIM4_IRQn 1 */
}

